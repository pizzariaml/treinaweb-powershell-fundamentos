# Operadores Atribuição

# Atribuição
$a = 10
Write-Host "Atribuição: $a"

# Adição
$a += 5
Write-Host "Adição: $a"

# Subtração
$a -= 5
Write-Host "Subtração: $a"

# Multiplicação
$a *= 5
Write-Host "Multiplicação: $a"

# Divisão
$a /= 5
Write-Host "Divisão: $a"

# Modulo
$a %= 3
Write-Host "Modulo: $a"