# Operadores

## Operadores Aritméticos
$a = 10
$b = 3

$soma = $a + $b
Write-Host "Soma: $soma"

$subtracao = $a - $b
Write-Host "Subtração: $subtracao"

$divisao = $a / $b
Write-Host "Divisão: $divisao"

$multiplicacao = $a * $b
Write-Host "Multiplicação: $multiplicacao"

$modulo = $a % $b
Write-Host "Modulo: $modulo"

$incremento = ++$a
Write-Host "Incremento: $incremento"

$decremento = --$a
Write-Host "Decremento: $decremento"